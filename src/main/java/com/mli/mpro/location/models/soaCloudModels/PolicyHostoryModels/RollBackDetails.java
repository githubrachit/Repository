public class RollBackDetails {

    public String isRollback;
    public String rollbackDate;
    public String rollbackReason;

    public String getIsRollback() {
        return isRollback;
    }

    public void setIsRollback(String isRollback) {
        this.isRollback = isRollback;
    }

    public String getRollbackDate() {
        return rollbackDate;
    }

    public void setRollbackDate(String rollbackDate) {
        this.rollbackDate = rollbackDate;
    }

    public String getRollbackReason() {
        return rollbackReason;
    }

    public void setRollbackReason(String rollbackReason) {
        this.rollbackReason = rollbackReason;
    }
}
